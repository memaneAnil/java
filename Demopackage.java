package Marvellous;

public class Demopackage
{
    public int iValue1;
    public int iValue2;

    public Demopackage(int a,int b)
    {
        iValue1=a;
        iValue2=b;
    }
    public int addition()
    {
        return iValue1+iValue2;
    }
    public int substraction()
    {
        return iValue1-iValue2;
    }
    
}